def make_tiny_mp4(path: Path) -> None:
    """Create a 1-second minimal mp4 using ffmpeg lavfi."""
    path.parent.mkdir(parents=True, exist_ok=True)
    subprocess.run(
        [
            "ffmpeg", "-y",
            "-f", "lavfi",
            "-i", "color=black:size=32x32:rate=1",
            "-t", "1",
            "-c:v", "libx264",
            str(path),
        ],
        check=True,
        capture_output=True,
    )


def make_solid_jpg(path: Path, color=(20, 20, 20)) -> None:
    """Create a tiny solid-colour JPEG using Pillow."""
    try:
        from PIL import Image
    except ImportError:
        pytest.skip("Pillow not available — cannot generate fixture images")
    path.parent.mkdir(parents=True, exist_ok=True)
    Image.new("RGB", (120, 80), color=color).save(str(path))


